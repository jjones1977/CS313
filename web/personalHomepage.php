        <h2>The Basics</h2>
        <div class="div1">
            <ul class="a">
            <li>A game of Bunco is played in two to four sets, with six rounds in each set.</li>
            <li>A player at the head table rings a bell to signal the beginning and end of each round.</li>
            <li>During the game, players at each table take turns rolling three dice to try and earn points. All tables play simultaneously.</li>
            <li>During each round, players attempt to roll the same number as the number of the round.</li>
            <li>For every number rolled that matches the round number, one point is awarded to that player.</li>
            <li>A player keeps rolling until they score no points. The scorekeeper then records the score they earned onto the table tally as the temporary team score. Then the dice are passed to the player on the left.</li>
            <li>When the head table has earned 21 points, the round is over. The head table rings a bell to signal that the round has ended.</li>
            <li>At the end of every round, players change partners and/or tables. </li>
            <li>At the end of a game, there are typically multiple "winners." The player(s) with the most Buncos, wins, losses, etc, may earn prizes for their successes.</li>
            </ul>
