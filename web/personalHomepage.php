        <h2>What is Bunco?</h2>
            <div class="div2">
                Bunco is a social dice game, traditionally played with 12 players who are divided into three tables with four players at each table. But really, almost any number can play. And if the number of players is not divisible by 4, tack on a ghost player, or two.
